    private void buBuscarAluguelActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buBuscarAluguelActionPerformed
        if(edIdAluguel.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Erro! Campo de busca vazio!", "Informações do Patins", JOptionPane.INFORMATION_MESSAGE);           
        } else{
            int id = Integer.parseInt(edIdAluguel.getText());
            Aluguel aluguel = controladorAluguel.selecionarAluguelPorId(id);

            if(aluguel != null){
                System.out.println("Aluguel existe!");
                JOptionPane.showMessageDialog(null, aluguel.retornaAluguel(), "Informações do Patins", JOptionPane.INFORMATION_MESSAGE);
            } else{
                System.out.println("Aluguel não existe!");
                JOptionPane.showMessageDialog(null, "Não existe esse aluguel, confira o Id!", "Informações do Patins", JOptionPane.INFORMATION_MESSAGE);            
            }
        }
    }//GEN-LAST:event_buBuscarAluguelActionPerformed

    private void buCalcularValorTotalActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buCalcularValorTotalActionPerformed
        int id = Integer.parseInt(edIdAluguel.getText());
        Aluguel aluguel = controladorAluguel.selecionarAluguelPorId(id);
        
        Patins patins = aluguel.obterPatins();
        boolean danificado = Boolean.parseBoolean(edDanificado.getText());
        float precoAdicional;
        float precoPatins = patins.obterPreco();
        float precoTotal;
        System.out.println(String.format("Preco do Patins %.2f",precoPatins));
        
        if (danificado == true){
            precoAdicional = Float.parseFloat(edPrecoAdicional.getText());
            System.out.println(String.format("Taxa de dano %.2f",precoAdicional));
            precoTotal = precoAdicional + precoPatins;
        } else{
            precoTotal = precoPatins;
        }
        System.out.println(String.format("Preco Final do Patins %.2f",precoTotal));
        laValorTotalAluguel.setText(String.format("%.2f", precoTotal));
    }//GEN-LAST:event_buCalcularValorTotalActionPerformed

