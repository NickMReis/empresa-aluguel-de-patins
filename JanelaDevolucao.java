    private void buConfirmarDevolucaoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buConfirmarDevolucaoActionPerformed
        Devolucao devolucao = new Devolucao();
        Pagamento pagamento = new Pagamento();
        
        int idAluguel = Integer.parseInt(edIdAluguel.getText());
        
        //adicionado para verificar se tem patins
        Aluguel aluguel = controladorAluguel.selecionarAluguelPorId(idAluguel);
        Patins patins = aluguel.obterPatins();
        
        if(aluguel != null){
            devolucao.alterarAluguel(aluguel);
            devolucao.alterarHoraFim(edHoraFim.getText());
            devolucao.alterarDanificado(Boolean.parseBoolean(edDanificado.getText()));
            if(!edPrecoAdicional.getText().isEmpty() || Boolean.parseBoolean(edDanificado.getText()) == true){
                devolucao.alterarPrecoAdicionalDano(Float.parseFloat(edPrecoAdicional.getText()));
            } else{
                devolucao.alterarPrecoAdicionalDano(0);
            }
            devolucao.alterarValorAluguel(patins.obterPreco());
            devolucao.alterarFormaPagamento(edFormaPagamento.getText());
            devolucao.alterarStatusDevolucao(true);
                        
            pagamento.alterarIdPagamento(controladorPagamento.obterTamanhoPagamento() + 1);
            pagamento.alterarDevolucao(devolucao);
            if(!edPrecoAdicional.getText().isEmpty() || Boolean.parseBoolean(edDanificado.getText()) == true){
                pagamento.alterarValorPagamento(Float.parseFloat(edPrecoAdicional.getText()) + patins.obterPreco());
            } else{
                pagamento.alterarValorPagamento(patins.obterPreco());                
            }
            pagamento.alterarFormaPagamento(edFormaPagamento.getText());
            
            controladorPagamento.incluirPagamento(pagamento);
            controladorAluguel.excluirAluguel(aluguel);
            System.out.println("Devolucao e Pagamento Adicionado a Lista!");
            System.out.println(controladorPagamento.obterPagamentos());
            
            JOptionPane.showMessageDialog(null, "Devolução realizada com sucesso!", "Informações da Devolução", JOptionPane.INFORMATION_MESSAGE);
           
        } else{
            JOptionPane.showMessageDialog(null, "Erro ao fazer a Devolucao! Confira o Id!", "Aviso da Devolução", JOptionPane.INFORMATION_MESSAGE);
        }
        
        limparCamposDevolucao();
    }//GEN-LAST:event_buConfirmarDevolucaoActionPerformed

    private void buMostrarAlugueisActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buMostrarAlugueisActionPerformed
        int idAluguel;
        String itemAluguel;
        String mensagemAluguel = "";
        
        for (int i = 0; i < controladorAluguel.obterTamanho(); i++){
            idAluguel = i + 1;
            Aluguel aluguel = controladorAluguel.selecionarAluguelPorId(idAluguel);
            
            if(aluguel != null){
                itemAluguel = aluguel.retornaAluguel();
                mensagemAluguel = mensagemAluguel + itemAluguel + "\n\n";
            } 
        }
        if(mensagemAluguel.equals("")){
            JOptionPane.showMessageDialog(null, "Não há alugueis em andamento!", "Alugueis em andamento", JOptionPane.INFORMATION_MESSAGE);         
        } else{
            JOptionPane.showMessageDialog(null, mensagemAluguel, "Alugueis em andamento", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_buMostrarAlugueisActionPerformed
    
